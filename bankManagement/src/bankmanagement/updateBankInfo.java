    public boolean checkValidPersonalLoanDetail() {
        if (jTextField1.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Please Enter a Time Period .", "failed", 2);
            return false;
        } else {
            timePeriod = Integer.parseInt(jTextField1.getText());
            if (jTextField2.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Please Enter an Interest Rate .", "failed", 2);
                return false;
            } else {
                if (jTextField2.getText().matches("^\\d+\\.\\d+")) {
                    ir = Double.parseDouble(jTextField2.getText());
                    return true;
                } else {
                    JOptionPane.showMessageDialog(null, "Please Enter a Valid Interest Rate in double format!!!", "Error", 2);
                    jTextField2.setText("");
                    return false;
                }
            }
        }
    }

