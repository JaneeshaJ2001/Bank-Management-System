    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (checkValidAccNumber()) {
            try {
                PreparedStatement pa;
                ResultSet rs;

                String query = "SELECT * FROM `customerlogin` WHERE `accountNumber` = ?";
                pa = conn.prepareStatement(query);
                pa.setInt(1, receiverAccNumber);
                rs = pa.executeQuery();

                accType = Integer.parseInt(rs.getString(7));
                if (accType != 2) {
                    if (rs.next()) {
                        jTextField7.setText(rs.getString(2));
                    } else {
                        JOptionPane.showMessageDialog(null, "This Account Number Doesn't Exist\nTry Again!!!", "Error", 2);
                        jTextField5.setText("");
                    }
                }else{
                    JOptionPane.showMessageDialog(null, "This Account Number Doesn't Respond To Do Transactions\nTry Again!!!", "Error", 2);
                }
            } catch (SQLException ex) {
                Logger.getLogger(transaction.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private boolean checkValidAccNumber() {
        if (jTextField5.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Please Enter Receiver's Account Number", "Error", 2);
            return false;
        } else if (!jTextField5.getText().matches(".*\\d.*")) {
            JOptionPane.showMessageDialog(null, "Please Enter a Valid Account Number !!!", "Error", 2);
            jTextField5.setText("");
            return false;
        } else if (jTextField5.getText().length() != 4) {
            JOptionPane.showMessageDialog(null, "This Account Number Doesn't Valid !", "Error", 2);
            jTextField5.setText("");
            return false;
        } else {
            receiverAccNumber = Integer.parseInt(jTextField5.getText());
            return true;
        }
    }

