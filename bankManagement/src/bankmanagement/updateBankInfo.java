    private void addBtnBranchDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnBranchDetailsActionPerformed
        // TODO add your handling code here:
        if (checkValidBranchDetail()) {
            try {
                PreparedStatement paa;

                String query = "INSERT INTO `branches` (`id` ,`city` , `district` ,`contactNumber` ) VALUES (?,?,?,?)";
                paa = conn.prepareStatement(query);
                paa.setInt(1, id);
                paa.setString(2, city);
                paa.setString(3, district);
                paa.setString(4, contactNumber);
                if (paa.executeUpdate() != 0) {
                    addTable4();
                    resetValue();
                    JOptionPane.showMessageDialog(null, "Details added Successfully");
                } else {
                    JOptionPane.showMessageDialog(null, "Details aren't successfully added", "failed", 2);
                }
            } catch (SQLException ex) {
                Logger.getLogger(updateBankInfo.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_addBtnBranchDetailsActionPerformed

