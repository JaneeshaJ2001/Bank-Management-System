    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy-MM-dd HH-mm-ss");
        LocalDateTime now = LocalDateTime.now();
        date = dtf.format(now);

        if (!(jTextField7.getText().equals(""))) {
            if (validTransferAmount()) {
                try {
                    PreparedStatement pa;

                    String query = "INSERT INTO `accStatement` (`accountNumber` ,`description` ,`referenceNumber` ,`debit` , `credit` , `balance` , `date`) VALUES (?,?,?,?,?,?,?)";
                    pa = conn.prepareStatement(query);
                    pa.setInt(1, receiverAccNumber);
                    pa.setString(2, "transaction");
                    pa.setInt(3, 0);
                    pa.setDouble(4, transferAmount);
                    pa.setDouble(5, 0.0);
                    pa.setDouble(6, balance + transferAmount);
                    pa.setString(7, date);

                    if (pa.executeUpdate() != 0) {
                        enterTransferData();
                    } else {
                        JOptionPane.showMessageDialog(null, "Details aren't successfully recorded", "failed", 2);
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(transaction.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "After entering the account number, click on search icon ", "failed", 2);
        }
    }

    private void enterTransferData() {
        try {
            PreparedStatement paa;

            String query = "INSERT INTO `accStatement` (`accountNumber` ,`description` ,`referenceNumber` ,`debit` , `credit` , `balance` , `date`) VALUES (?,?,?,?,?,?,?)";
            paa = conn.prepareStatement(query);
            paa.setInt(1, accNumber);
            paa.setString(2, "transaction");
            paa.setInt(3, 0);
            paa.setDouble(4, 0.0);
            paa.setDouble(5, transferAmount);
            paa.setDouble(6, balance - transferAmount);
            paa.setString(7, date);

            if (paa.executeUpdate() != 0) {
                JOptionPane.showMessageDialog(null, "Your Transaction has been Succfully Performed !");
                clearData();
                getBalance();
            } else {
                JOptionPane.showMessageDialog(null, "Details aren't successfully recorded", "failed", 2);
            }
        } catch (SQLException ex) {
            Logger.getLogger(transaction.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private boolean validTransferAmount() {
        transferAmount = Double.parseDouble(jTextField6.getText());

        if (jTextField6.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Please Enter Transferring Amount", "Error", 2);
            return false;
        } else if (!jTextField6.getText().matches("^\\d+\\.\\d+")) {
            JOptionPane.showMessageDialog(null, "Please Enter a Valid Amount in double format!!!", "Error", 2);
            jTextField6.setText("");
            return false;
        } else if (balance <= transferAmount) {
            JOptionPane.showMessageDialog(null, "Your Current Balance Isn't Enough To Do A Transaction !", "Error", 2);
            return false;
        } else {
            return true;
        }
    }//GEN-LAST:event_jButton1ActionPerformed

