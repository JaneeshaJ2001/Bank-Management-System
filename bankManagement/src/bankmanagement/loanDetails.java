    public void addTable() {
        PreparedStatement pa;
        ResultSet rs;

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        String sql = "SELECT * FROM `loanacceptors` ";
        try {
            pa = conn.prepareStatement(sql);
            rs = pa.executeQuery();
            while (rs.next()) {

                accNumber = Integer.parseInt(rs.getString(2));
                accName = getAccName(accNumber);
                int loanType = Integer.parseInt(rs.getString(5));
                
                if (loanType == 0) {
                    typeOfLoan = "Personal";
                } else if (loanType == 1) {
                    typeOfLoan = "Home";
                }
                model.addRow(new String[]{rs.getString(2), accName, rs.getString(9), typeOfLoan, rs.getString(8), rs.getString(10)});
            }
        } catch (SQLException ex) {
            Logger.getLogger(loanDetails.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public String getAccName(int accNumber) {
        PreparedStatement pa;
        ResultSet rs;

        String query = "SELECT * FROM `customerlogin` WHERE `accountNumber` = ?";
        try {
            pa = conn.prepareStatement(query);
            pa.setInt(1, accNumber);
            rs = pa.executeQuery();

            if (rs.next()) {
                accName = rs.getString(2);
                return accName;
            }
        } catch (SQLException ex) {
            Logger.getLogger(loanDetails.class.getName()).log(Level.SEVERE, null, ex);
        }
        return null;
    }

    public void totalLoanAcceptors() {
        try {
            PreparedStatement pa;
            ResultSet rs;

            String query = "SELECT COUNT(*) FROM `loanacceptors` ";
            pa = conn.prepareStatement(query);
            rs = pa.executeQuery();

            if (rs.next()) {
                jTextField1.setText(rs.getString(1));
            }
        } catch (SQLException ex) {
            Logger.getLogger(loanDetails.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
