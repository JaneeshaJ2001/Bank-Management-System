    public boolean checkValidBranchDetail() {
        if (jTextField5.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Please Enter Branch Id Number .", "failed", 2);
            return false;
        } else {
            id = Integer.parseInt(jTextField5.getText());
            if (jTextField6.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Please Enter Name Of City .", "failed", 2);
                return false;
            } else {
                city = jTextField6.getText();
                if (jTextField7.getText().equals("")) {
                    JOptionPane.showMessageDialog(null, "Please Enter Name Of District .", "failed", 2);
                    return false;
                } else {
                    district = jTextField7.getText();
                    if (jTextField8.getText().equals("")) {
                        JOptionPane.showMessageDialog(null, "Please Enter Contact Number .", "failed", 2);
                        return false;
                    } else {
                        contactNumber = jTextField8.getText();
                        return true;
                    }
                }
            }
        }
    }

